	<h2>Ingredients</h2>
	<ul>
		<li>225g plain flour</li>
		<li>100g unsalted butter, cold and diced</li>
		<li>75g caster sugar</li>
		<li>1 tsp vanilla extract (optional)</li>
		<li>1–2 tbsp milk (if needed)</li>
	</ul>

	<h2>Method</h2>
	<ol>
		<li>Preheat your oven to 180°C (160°C fan) / 350°F / Gas 4. Line a baking tray with parchment.</li>
		<li>In a large bowl, rub the butter into the flour using your fingertips until it looks like breadcrumbs.</li>
		<li>Stir in the sugar (and vanilla if using).</li>
		<li>Bring the dough together – it should come together when squeezed. If it’s too crumbly, add a splash of milk, 1 tbsp at a time.</li>
		<li>Roll out the dough on a lightly floured surface to about 5mm thick.</li>
		<li>Cut into shapes using your favourite biscuit cutter (round, hearts, whatever you like).</li>
		<li>Place on the tray and bake for 12–15 minutes until lightly golden at the edges.</li>
		<li>Let them cool on the tray for a few minutes, then transfer to a wire rack.</li>
	</ol>
